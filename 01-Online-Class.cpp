// sizeof() Operation on different types of variables
#include<iostream>
using namespace std;
int main()
{
    //Creating Variables for different basic data type.
    char ip1;
    int ip2;
    float ip3;
    double ip4;

    cout<<"sizeof(ip1) : "<<sizeof(ip1)<<endl;
    cout<<"sizeof(ip2) : "<<sizeof(ip2)<<endl;
    cout<<"sizeof(ip3) : "<<sizeof(ip3)<<endl;
    cout<<"sizeof(ip4) : "<<sizeof(ip4)<<endl;

return 0;
}

