// Arithmetic Operations on double variables | Binary and Unary
#include<iostream>
using namespace std;
int main()
{
    double ip1, ip2;

    cout<<"Enter value for ip1 : ";     cin>>ip1;
    cout<<"Enter value for ip2 : ";     cin>>ip2;

    cout<<"-ip1 : "<<-ip1<<endl;
    cout<<"ip1 + ip2 : "<<ip1+ip2<<endl;
    cout<<"ip1 - ip2 : "<<ip1-ip2<<endl;
    cout<<"ip1 * ip2 : "<<ip1*ip2<<endl;
    cout<<"ip1 / ip2 : "<<ip1/ip2<<endl;

return 0;
}
